"""Выгрузка схемы OpenAPI `/v1` в файл репозитория (эпик API-1).

Схема — обещание, выданное наружу тому, у кого есть токен. Пока она
существовала только в живом процессе, поменять обещание можно было незаметно:
правка роутера уезжала в дифф как код, а изменение контракта не показывал никто.
Поэтому схема лежит в репозитории отдельным файлом ``contract/openapi.json`` и
проходит ревью как обычный дифф — сломанная совместимость видна глазами до
выката, а не по жалобе клиента после.

Сериализация нарочно жёстко детерминированная (сортировка ключей, фиксированный
отступ, один перевод строки в конце). Иначе смысл затеи теряется: файл, который
меняется сам по себе от запуска к запуску, даёт шумный дифф и приучает
проглатывать красные ворота не глядя.

Ни база, ни сеть, ни секреты здесь не нужны: приложение собирает роуты сразу, а
зависимости роутера (движок БД, проверка роли) собираются лениво, уже внутри
первого запроса. Значит, команду можно звать на любой машине и в любом CI без
подготовки окружения.
"""

import json
from pathlib import Path

from second_brain.bootstrap.app import create_app

# Файл артефакта относительно корня репозитория (пакет ставится editable, так
# что путь модуля ведёт в исходники, а не в копию внутри venv).
CONTRACT_PATH = Path(__file__).resolve().parents[3] / "contract" / "openapi.json"


def render_contract() -> str:
    """Собрать текст артефакта — ровно тот, что ожидается в файле."""
    schema = create_app().openapi()
    return json.dumps(schema, indent=2, sort_keys=True, ensure_ascii=False) + "\n"


def main() -> None:
    """Точка входа команды ``second-brain-openapi``: переписать артефакт."""
    CONTRACT_PATH.parent.mkdir(parents=True, exist_ok=True)
    CONTRACT_PATH.write_text(render_contract(), encoding="utf-8")
    print(f"wrote {CONTRACT_PATH}")


if __name__ == "__main__":
    main()
